// 8. Write a program to dispaly number from 1-20 using all types of loops
//For Loop :-
for (let i = 1; i <= 20; i++) {
  console.log(i);
}
//While Loop:-
let num = 1;
while (num <= 20) {
  console.log(num);
  num++;
}
//Do-While Loop:-
num = 1;
do {
  console.log(num);
  num++;
} while (num <= 20);
